package filestream;
import java.io.*;
public class Stddetails {
	int stdage;
	String stdname;
	float stdfees;
	char stdgender;
	
	
	public String getstdname()
	{
		return stdname;
	}
	
	public void setstdname(String stdname)
	{
		this.stdname = stdname;
	}
	public float getstdfees()
	{
		return stdfees;
	}
	
	public void setstdfees(float stdfees)
	{
		this.stdfees = stdfees;
	}
	public char getstdgender()
	{
		return stdgender;
	}
	
	public void setstdgender(char stdgender)
	{
		this.stdgender = stdgender;
	}
	public int getstdage()
	{
		return stdage;
	}
	
	public void setstdage(int stdage)
	{
		this.stdage = stdage;
		
	}
}
	
	
	
		
	
		